// Arithematic Operators

let a = 3;
let b = 2;
console.log("a =", a ,"& b =", b);  // Prints a & b
console.log("a + b = ",a+b);    // Adding a and b
console.log("a - b = ",a-b);    // Subtracting a and b
console.log("a * b = ",a*b);    // Multiplying a and b
console.log("a / b = ",a/b);    // Dividing a and b
console.log("a % b = ",a%b);    // Modulus operators (returns remainder)
console.log("a ** b = ",a**b);  // ** is power operator (a^b)

// Unary Operators

a++;    // increment operator
b--;    // Decrement operator
console.log("a = ",a);
console.log("b = ",b);