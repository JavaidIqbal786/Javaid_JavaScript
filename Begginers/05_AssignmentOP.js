// Assignment Operators (=, +=, -=, *=, /=, %=, **=)

let a = 5;
let b = 3;

console.log("a = ",a += b ); // a = a + b Expected Output a = 8
console.log("a = ", a -= b); // a = a - b Expected Output a = 5
console.log("a = ", a *= b); // a = a * b Expected Output a = 15
console.log("a = ", a /= b); // a = a / b Expected Output a = 5
console.log("a = ", a %= b); // a = a % b Expected Output a = 2
console.log("a = ", a **= b); // a = a ** b Expected Output a = 8